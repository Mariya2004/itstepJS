class Car {
    model;
    number;
    color;
    comfort;
    hasBabyChair;
    capacity;

    constructor (model, number, color, comfort, hasBabyChair, capacity) {
        this.model = model;
        this.number = number;
        this.color = color;
        this.comfort = comfort;
        this.hasBabyChair = hasBabyChair;
        this.capacity = capacity;
    }
}
export{Car};